'use client';

import styles from './ΠλαίσιοΜορφής.module.css';

import { useAppContext } from '@/context/AppContext';

const LooksContainer = ({
  καθορισμόςΔιαστάσεων,
  ακτίναΓωνίας,
  πάχοςΟρίου,
  ενεργόςΔιακόπτηςΦέγγουςΟρίων,
  ενεργόςΔιακόπτηςΦέγγουςΣτοιχείων,
  pxΎψουςΓιαΑπόκρυψη,
  pxΠλάτουςΓιαΑπόκρυψη,
  children,
}) => {
  const { μορφήΣτοιχείων, φέγγοςΟρίων, φέγγοςΣτοιχείων } = useAppContext();

  const ακτίνα =
    ακτίναΓωνίας === 15
      ? 'ακτίνα-γωνίας-15'
      : ακτίναΓωνίας === 25
        ? 'ακτίνα-γωνίας-25'
        : ακτίναΓωνίας === 35
          ? 'ακτίνα-γωνίας-35'
          : '';

  const πάχος =
    πάχοςΟρίου === 0
      ? 'πάχος-ορίου-0'
      : πάχοςΟρίου === 1
        ? 'πάχος-ορίου-1'
        : πάχοςΟρίου === 2
          ? 'πάχος-ορίου-2'
          : πάχοςΟρίου === 3
            ? 'πάχος-ορίου-3'
            : '';

  const πλάτοςΑπόκρυψης = pxΠλάτουςΓιαΑπόκρυψη
    ? pxΠλάτουςΓιαΑπόκρυψη === 480
      ? 'απόκρυψη-σε-πλάτος-480px'
      : pxΠλάτουςΓιαΑπόκρυψη === 768
        ? 'απόκρυψη-σε-πλάτος-768px'
        : pxΠλάτουςΓιαΑπόκρυψη === 1024
          ? 'απόκρυψη-σε-πλάτος-1024px'
          : pxΠλάτουςΓιαΑπόκρυψη === 1366
            ? 'απόκρυψη-σε-πλάτος-1366px'
            : 'απόκρυψη-σε-πλάτος-1366px'
    : '';

  const ύψοςΑπόκρυψης = pxΎψουςΓιαΑπόκρυψη
    ? pxΎψουςΓιαΑπόκρυψη === 550
      ? 'απόκρυψη-σε-ύψος-550px'
      : pxΎψουςΓιαΑπόκρυψη === 768
        ? 'απόκρυψη-σε-ύψος-768px'
        : pxΎψουςΓιαΑπόκρυψη === 1024
          ? 'απόκρυψη-σε-ύψος-1024px'
          : pxΎψουςΓιαΑπόκρυψη === 1366
            ? 'απόκρυψη-σε-ύψος-1366px'
            : 'απόκρυψη-σε-ύψος-1366px'
    : '';

  return (
    <div
      className={
        styles['πλαίσιο-μορφής'] +
        ' ' +
        styles[
          `${καθορισμόςΔιαστάσεων === 'από έξω' ? 'διαστάσεις-από-έξω' : καθορισμόςΔιαστάσεων === 'από μέσα' ? 'διαστάσεις-από-μέσα' : 'αρχική-ρύθμιση-διαστάσεων'}`
        ] +
        ' ' +
        styles[`${ακτίνα}`] +
        ' ' +
        styles[`${πάχος}`] +
        ' ' +
        styles[
          `${μορφήΣτοιχείων === 'υαλομορφισμός' ? 'με-υαλομορφισμό' : μορφήΣτοιχείων === 'ημιδιαφανή στοιχεία' ? 'με-ημιδιαφανή-στοιχεία' : μορφήΣτοιχείων === 'αδιαφανή στοιχεία' ? 'με-αδιαφανή-στοιχεία' : ''}`
        ] +
        ' ' +
        styles[
          `${φέγγοςΟρίων === true ? (ενεργόςΔιακόπτηςΦέγγουςΟρίων ? 'με-φέγγος-ορίων' : '') : ''}`
        ] +
        ' ' +
        styles[
          `${φέγγοςΣτοιχείων === true ? (ενεργόςΔιακόπτηςΦέγγουςΣτοιχείων ? 'με-φέγγος-στοιχείων' : '') : ''}`
        ] +
        ' ' +
        styles[`${ύψοςΑπόκρυψης}`] +
        ' ' +
        styles[`${πλάτοςΑπόκρυψης}`]
      }
    >
      {children}
    </div>
  );
};

export default LooksContainer;
