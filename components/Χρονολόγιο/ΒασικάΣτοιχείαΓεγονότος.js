import styles from '@/components/Χρονολόγιο/ΣτοιχείαΧρονολογίου.module.css';

const ΒασικάΣτοιχείαΓεγονότος = ({
  γεγονόςΣεΠεριττόΑριθμό = { γεγονόςΣεΠεριττόΑριθμό },
  μεΉΧωρίςΔευτερεύουσαΓραμμή,
  children,
}) => {
  const περιττόΉΆρτιοΓεγονός = γεγονόςΣεΠεριττόΑριθμό
    ? 'βασικές-πληροφορίες-περιττού-γεγονότος'
    : 'βασικές-πληροφορίες-άρτιου-γεγονότος';

  return (
    <div
      className={
        styles[`${περιττόΉΆρτιοΓεγονός}`] +
        ' ' +
        styles[`${μεΉΧωρίςΔευτερεύουσαΓραμμή}`]
      }
    >
      {children}
    </div>
  );
};

export default ΒασικάΣτοιχείαΓεγονότος;
